/// <summary>
/// Different planes to be encoded/decoded in DCT coefficient decoding.
/// </summary>
internal enum Plane
{
    /// <summary>The Y plane after decoding Y2</summary>
    YCoeff1 = 0,
    /// <summary>The Y2 plane (specifies the 0th coefficient of the other Y blocks)</summary>
    Y2 = 1,
    /// <summary>The U or V plane</summary>
    Chroma = 2,
    /// <summary>The Y plane when there is no Y2 plane</summary>
    YCoeff0 = 3,
}

